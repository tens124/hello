package boss.model;

public class MainImage {

	private int mainid;
	private int pid;
	private String mainimage; 
	private String mainpname;
	private String maincontent;
	private int mainreadcount;
	
	public int getMainid() {
		return mainid;
	}
	public void setMainid(int mainid) {
		this.mainid = mainid;
	}
	public int getPid() {
		return pid;
	}
	public void setPid(int pid) {
		this.pid = pid;
	}
	public String getMainimage() {
		return mainimage;
	}
	public void setMainimage(String mainimage) {
		this.mainimage = mainimage;
	}
	public String getMainpname() {
		return mainpname;
	}
	public void setMainpname(String mainpname) {
		this.mainpname = mainpname;
	}
	public String getMaincontent() {
		return maincontent;
	}
	public void setMaincontent(String maincontent) {
		this.maincontent = maincontent;
	}
	public int getMainreadcount() {
		return mainreadcount;
	}
	public void setMainreadcount(int mainreadcount) {
		this.mainreadcount = mainreadcount;
	}
	
	
	
}







